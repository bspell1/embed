//-----------< FUNCTION: DebugInit >-----------------------------------------
// Purpose:    debug initializer
//             disables the watchdog timer, if it was set by an assert
// Parameters: none
// Returns:    none
//---------------------------------------------------------------------------
__attribute__((naked))
__attribute__((used))
__attribute__((section(".init8")))
void DebugInit ()
{  
   MCUSR &= ~(1 << WDRF);
   wdt_disable(); 
}
